x = 3.14
y = -4
z = 5

# Round function
result = round(x)

# Abs function = The distance of a number from 0
result2 = abs(y)

# Power function. Left digit base(5), Right digit exponent(3)
result3 = pow(5, 3)

# Finds Max and Min number in a set
result4 = max(x, y, z)
result5 = min(x, y, z)

print(result)
print(result2)
print(result3)
print(result4)
print(result5)