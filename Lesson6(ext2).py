import math

x = 9
y = 3.1
z = 6.8

# prints the value of pi (3.14...)
print(f"The value of Pi is {math.pi}")

# prints the square root of a number
result = math.sqrt(x)

# Ceil function rounds a value up, always
result2 = math.ceil(y)

# Floor functions rounds a value down, always
result3 = math.floor(z)

print(f"The Square root of 9 is {result}")
print(f"The Ceiling value of 3.1 is {result2}")
print(f"The Floor value of 6.8 is {result3}")